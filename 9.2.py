def find_max_sequence():
    """
    Считывает последовательность натуральных чисел с клавиатуры до ввода 0.
    Возвращает наибольшее значение в последовательности.
    Не использует параметры, циклы или глобальные переменные.
    """
    
    # 1. Считывание текущего числа
    try:
        # Считываем строку и преобразуем в целое число
        current_num = int(input())
    except EOFError:
        # Если ввод неожиданно прервался (например, при перенаправлении файла)
        return 0
    except ValueError:
        # Если введено не число, можно обработать ошибку или считать это 0
        return 0 

    # 2. Базовый случай (Условие завершения)
    # Если введено 0, последовательность закончилась. 
    # Возвращаем 0, так как это число не может быть максимумом 
    # (по условию, последовательность содержит хотя бы одно натуральное число).
    if current_num == 0:
        return 0

    # 3. Рекурсивный шаг
    # Рекурсивно вызываем функцию, чтобы найти максимум из оставшейся части последовательности
    max_of_rest = find_max_sequence()

    # 4. Сравнение и возврат
    # Сравниваем текущее число с максимумом, найденным в остатке последовательности
    return max(current_num, max_of_rest)

# --- Пример использования ---
if __name__ == "__main__":
    print("Введите последовательность натуральных чисел (по одному в строке). Для завершения введите 0:")
    
    # Для выполнения этой части кода вам нужно будет вручную ввести числа 
    # в консоли, а в конце ввести 0.
    
    # Пример ввода:
    # 12
    # 5
    # 30
    # 18
    # 0
    
    try:
        maximum_value = find_max_sequence()
        print(f"\nНаибольшее значение в последовательности: {maximum_value}")
    except RecursionError:
        print("Ошибка: Превышена глубина рекурсии. Последовательность слишком длинная.")